// Пример 1: Escape analysis — компилятор решает, где жить переменной.
//
// В Go можно безопасно возвращать указатель на локальную переменную!
// В отличие от C, где это — висячий указатель (dangling pointer),
// компилятор Go анализирует, «убегает» ли переменная из функции.
// Если да — она автоматически размещается в куче, а не на стеке.
//
// Флаги компилятора (-gcflags):
//   -m       — показать решения escape analysis и инлайнинга (1 уровень).
//   -m -m    — подробнее: причины, почему переменная убежала (2 уровень).
//   -m -m -m — максимальная детализация (3 уровень).
//   -l       — отключить инлайнинг. Без этого компилятор встраивает
//              маленькие функции в вызывающий код, и escape-решения
//              «растворяются» — не видно, что именно убежало.
//
// Сборка с выводом решений escape analysis:
//   go build -gcflags="-m" .
//
// Более подробный вывод:
//   go build -gcflags="-m -m" .
//
// Без инлайнинга (чтобы увидеть escape на уровне каждой функции):
//   go build -gcflags="-m -l" .
//
// Запуск:
//   go run main.go

package main

import "fmt"

type Point struct {
	X, Y int
}

// newPoint — возвращает указатель на локальную переменную.
// В C это было бы ошибкой (return &local), но в Go — нормально.
// Компилятор увидит, что p «убегает» из функции, и разместит её в куче.
func newPoint(x, y int) *Point {
	p := Point{X: x, Y: y} // escape analysis: "moved to heap"
	return &p
}

// newPointStack — возвращает копию структуры (не указатель).
// Ничего не убегает → компилятор оставит всё на стеке.
// Возврат по значению — копирование, но для маленьких структур это дешевле,
// чем аллокация в куче + нагрузка на GC.
func newPointStack(x, y int) Point {
	p := Point{X: x, Y: y} // escape analysis: остаётся на стеке.
	return p
}

// sumPoint — принимает указатель, но не сохраняет его.
// Компилятор увидит, что p НЕ убегает → можно оставить на стеке.
func sumPoint(p *Point) int {
	return p.X + p.Y
}

// stayOnStack — все переменные локальные, ничего не убегает.
// Аллокация на стеке: бесплатно, не нагружает GC.
func stayOnStack() int {
	p := Point{X: 10, Y: 20} // Остаётся на стеке.
	return p.X + p.Y
}

// escapeToHeap — переменная убегает через интерфейс.
// fmt.Println принимает any (interface{}), и компилятор не может
// гарантировать, что значение не будет сохранено — поэтому кладёт в кучу.
func escapeToHeap() {
	p := Point{X: 30, Y: 40}
	fmt.Println(p) // p убегает в интерфейс → куча.
}

// escapeSlice — слайс слишком большой для стека → куча.
func escapeSlice() []int {
	// Маленькие слайсы могут остаться на стеке,
	// но большие — всегда в куче.
	s := make([]int, 1_000_000)
	return s
}

func main() {
	// Указатель на «локальную» переменную — безопасно, но куча.
	p := newPoint(1, 2)
	fmt.Printf("newPoint (куча):  %+v (адрес: %p)\n", *p, p)

	// Возврат по значению — копия, всё на стеке.
	ps := newPointStack(3, 4)
	fmt.Printf("newPointStack (стек): %+v\n", ps)

	// Передача указателя без побега — стек.
	local := Point{X: 5, Y: 6}
	fmt.Println("sumPoint:", sumPoint(&local))

	// Всё на стеке — GC вообще не задействован.
	fmt.Println("stayOnStack:", stayOnStack())

	// Убегание через интерфейс.
	escapeToHeap()

	// Большой слайс — куча.
	s := escapeSlice()
	fmt.Println("escapeSlice len:", len(s))

	fmt.Println("\nСоберите с go build -gcflags=\"-m\" . чтобы увидеть решения компилятора.")
}

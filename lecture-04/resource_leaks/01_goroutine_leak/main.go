// Утечка горутин: горутина блокируется навсегда, потому что
// никто не читает из канала / не закрывает канал.
//
// Сборка:
//   go build -o 01_goroutine_leak .
//
// Запуск:
//   ./01_goroutine_leak
//
// Запуск с valgrind:
//   valgrind --tool=memcheck ./01_goroutine_leak

package main

import (
	"fmt"
	"runtime"
	"time"
)

// leakyProducer запускает горутину, которая пишет в канал.
// Если никто не читает — горутина заблокируется навечно.
func leakyProducer() <-chan int {
	ch := make(chan int)
	go func() {
		// Горутина заблокируется на этой строке, если нет читателя.
		// Она никогда не завершится и не будет собрана GC.
		ch <- 42
		fmt.Println("Горутина завершилась") // Никогда не напечатается.
	}()
	return ch
}

// leakyConsumer запускает горутину, которая читает из канала.
// Если канал никогда не закроют — горутина навсегда ждёт.
func leakyConsumer(ch <-chan int) {
	go func() {
		for val := range ch {
			fmt.Println("Получено:", val)
		}
		fmt.Println("Читатель завершился") // Никогда не напечатается.
	}()
}

func main() {
	fmt.Printf("Горутин в начале: %d\n", runtime.NumGoroutine())

	// Пример 1: Производитель без потребителя.
	// Канал создан, но из него никто не читает → горутина утекла.
	for i := 0; i < 10; i++ {
		leakyProducer() // Возвращённый канал игнорируется.
	}

	// Пример 2: Потребитель без закрытия канала.
	// Канал создан, но его никто не закроет → читатель ждёт вечно.
	ch := make(chan int)
	for i := 0; i < 10; i++ {
		leakyConsumer(ch)
	}

	time.Sleep(100 * time.Millisecond)
	// 1 (main) + 10 (производители) + 10 (потребители) = 21 горутина.
	fmt.Printf("Горутин после утечки: %d\n", runtime.NumGoroutine())

	// В реальном приложении эти горутины будут накапливаться,
	// потребляя память и процессорное время, пока процесс не упадёт.
}

// Пример 5: Переполнение буфера (buffer overflow).
// Запись за пределы выделенного блока повреждает соседние данные
// или метаданные аллокатора. Классический источник уязвимостей.
//
// Запуск:
//   ./05_buffer_overflow
//
// Запуск с valgrind:
//   valgrind --leak-check=full --show-leak-kinds=all ./05_buffer_overflow

#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int main(void) {
    // Выделяем буфер на 10 байт.
    char *buf = malloc(10);
    if (!buf) {
        return 1;
    }

    // Копируем строку длиной > 10 байт — пишем за границу буфера!
    // strcpy не проверяет размер — классическая ошибка.
    strcpy(buf, "Это очень длинная строка, которая не поместится в 10 байт");
    // Перезаписаны метаданные аллокатора или соседние переменные.

    printf("buf = %s\n", buf);

    // free() может упасть, т.к. метаданные блока повреждены.
    free(buf);

    return 0;
}

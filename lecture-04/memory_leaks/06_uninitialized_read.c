// Пример 6: Чтение неинициализированной памяти.
// malloc() не обнуляет память — в ней лежит мусор от предыдущих аллокаций.
// Чтение без инициализации — неопределённое поведение.
//
// Запуск:
//   ./06_uninitialized_read
//
// Запуск с valgrind:
//   valgrind --leak-check=full --show-leak-kinds=all ./06_uninitialized_read

#include <stdio.h>

typedef struct {
    int x;
    int y;
    int z;
} Point;

// Вспомогательная функция «загрязняет» стек: записывает известные значения
// в локальный буфер. После возврата стековый фрейм остаётся «грязным».
void pollute_stack(void) {
    volatile int garbage[3];
    garbage[0] = 0xDEADBEEF;
    garbage[1] = 0xCAFEBABE;
    garbage[2] = 0x8BADF00D;
    (void)garbage;
}

// Выделяет Point на стеке (без инициализации) и читает поля.
// Стековый фрейм совпадает по расположению с pollute_stack() —
// в полях y и z окажется мусор от предыдущего вызова.
void read_uninitialized(void) {
    Point p;

    // Инициализируем только одно поле.
    p.x = 10;

    // Читаем неинициализированные поля — мусор от pollute_stack()!
    printf("x=0x%X (%d), y=0x%X (%d), z=0x%X (%d)\n",
           p.x, p.x, p.y, p.y, p.z, p.z);
}

int main(void) {
    // Шаг 1: «загрязняем» стек известными значениями.
    pollute_stack();

    // Шаг 2: читаем неинициализированную структуру — видим мусор.
    // Linux обнуляет страницы при выдаче процессу, поэтому при первом malloc()
    // память всегда нулевая. Но стек переиспользуется внутри процесса —
    // и в нём видны остатки предыдущих вызовов.
    read_uninitialized();

    // На куче эффект тот же, но его сложнее воспроизвести из-за tcache в glibc,
    // который затирает начало блока своими метаданными при free().

    // Правильный подход: всегда инициализировать переменные.
    // Для кучи: использовать calloc() вместо malloc(), или memset() после malloc().

    return 0;
}

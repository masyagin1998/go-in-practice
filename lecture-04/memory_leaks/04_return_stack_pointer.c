// Пример 4: Возврат указателя на локальную переменную (стек).
// Локальные переменные живут на стеке и уничтожаются при выходе из функции.
// Указатель на них после возврата — «висячий» (dangling pointer).
// Компилятор выдаст предупреждение, но код скомпилируется.
//
// Запуск:
//   ./04_return_stack_pointer
//
// Запуск с valgrind:
//   valgrind --leak-check=full --show-leak-kinds=all ./04_return_stack_pointer

#include <stdio.h>

int *get_value(void) {
    int x = 42;
    return &x; // ОШИБКА: возвращаем адрес локальной переменной!
    // После выхода из функции стековый фрейм будет перезаписан.
}

const char *get_name(void) {
    char name[] = "Иван"; // Массив на стеке.
    return name;           // ОШИБКА: указатель на стековую память!
}

int main(void) {
    int *val = get_value();
    // К этому моменту стековый фрейм get_value() уже не существует.
    // Значение *val — мусор (или случайно 42, если стек ещё не перезаписан).
    printf("Значение: %d\n", *val);

    const char *name = get_name();
    printf("Имя: %s\n", name); // Мусор или краш.

    return 0;
}

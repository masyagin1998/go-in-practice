#include "math.h"

// add_ints складывает два целых числа.
int add_ints(int a, int b) {
    return a + b;
}

// multiply_doubles перемножает два вещественных числа.
double multiply_doubles(double a, double b) {
    return a * b;
}

// is_positive проверяет, положительно ли число.
bool is_positive(int x) {
    return x > 0;
}

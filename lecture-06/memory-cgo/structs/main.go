package main

// Передача структур (без указателей) между Go и C.
// Структура C.Point не содержит указателей, поэтому
// копируется по стеку без каких-либо ограничений.

/*
#cgo LDFLAGS: -lm
#include "point.h"
*/
import "C"

import "fmt"

func main() {
	// Создаём C-структуру в Go — заполняем поля напрямую.
	p := C.Point{x: 3.0, y: 4.0}
	fmt.Printf("Точка: (%.1f, %.1f)\n", p.x, p.y)

	// Передаём структуру по значению — копия уходит в C.
	d := C.distance(p)
	fmt.Printf("Расстояние до (0,0): %.2f\n", d)

	// C-функция возвращает структуру — тоже копия по стеку.
	p2 := C.translate(p, 10, 20)
	fmt.Printf("После сдвига (10, 20): (%.1f, %.1f)\n", p2.x, p2.y)
}
